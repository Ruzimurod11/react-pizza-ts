import React from "react";
import { useSelector } from "react-redux";
import { useNavigate } from "react-router-dom";

//qs
import qs from "qs";

// Components

import {
   Skeleton,
   PizzaBlock,
   Categories,
   Sort,
   Pagination,
} from "../components";

import { sortList } from "../components/Sort";

import { useAppDispatch } from "../redux/store";
import { selectPizzaData } from "../redux/pizza/selectors";
import { selectFilter } from "../redux/filter/selectors";
import {
   setCategoryId,
   setCurrentPage,
   setFilters,
} from "../redux/filter/slice";
import { fetchPizzas } from "../redux/pizza/asyncActions";
import { SearchPizzaParams } from "../redux/pizza/types";

const Home: React.FC = () => {
   const navigate = useNavigate();
   const isSearch = React.useRef(false);
   const isMounted = React.useRef(false);

   // redux
   const dispatch = useAppDispatch();
   const { items, status } = useSelector(selectPizzaData);
   const { categoryId, currentPage, sort, searchValue } =
      useSelector(selectFilter);

   const onChangeCategory = React.useCallback((idx: number) => {
      dispatch(setCategoryId(idx));
   }, []);

   const onChangePage = (page: number) => {
      dispatch(setCurrentPage(page));
   };

   const getPizzas = async () => {
      const sortBy = sort.sortProperty.replace("-", "");
      const order = sort.sortProperty.includes("-") ? "asc" : "desc";
      const category = categoryId > 0 ? `category=${categoryId}` : "";
      const search = searchValue ? `&search=${searchValue}` : "";

      dispatch(
         fetchPizzas({
            sortBy,
            order,
            category,
            search,
            currentPage: String(currentPage),
         })
      );

      window.scrollTo(0, 0);
   };

   // –ï—Å–ª–∏ –∏–∑–º–µ–Ω–∏–ª–∏ –ø–∞—Ä–∞–º–µ—Ç—Ä—ã –∏ –±—ã–ª –ø–µ—Ä–≤—ã–π —Ä–µ–Ω–¥–µ—Ä
   React.useEffect(() => {
      if (isMounted.current) {
         const queryString = qs.stringify({
            sortProperty: sort.sortProperty,
            categoryId,
            currentPage,
         });
         navigate(`?${queryString}`);

         if (!window.location.search) {
            dispatch(fetchPizzas({} as SearchPizzaParams));
         }
      }
      isMounted.current = true;
   }, [categoryId, sort.sortProperty, searchValue, currentPage]);

   // –ï—Å–ª–∏ –±—ã–ª –ø–µ—Ä–≤—ã–π —Ä–µ–Ω–¥–µ—Ä, —Ç–æ –∑–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –ø–∏—Ü—Ü—ã
   React.useEffect(() => {
      getPizzas();
   }, [categoryId, sort.sortProperty, searchValue, currentPage]);

   // –ï—Å–ª–∏ –±—ã–ª –ø–µ—Ä–≤—ã–π —Ä–µ–Ω–¥–µ—Ä, —Ç–æ –ø—Ä–æ–≤–µ—Ä—è–µ–º URL –ø–∞—Ä–∞–º–µ—Ç—Ä—ã –∏ —Å–æ—Ö—Ä–∞–Ω—è–µ–º –≤ —Ä–µ–¥—É–∫—Å–µ
   React.useEffect(() => {
      if (window.location.search) {
         const params = qs.parse(
            window.location.search.substring(1)
         ) as unknown as SearchPizzaParams;
         const sort = sortList.find(
            (obj) => obj.sortProperty === params.sortBy
         );

         dispatch(
            setFilters({
               searchValue: params.search,
               categoryId: Number(params.category),
               currentPage: Number(params.currentPage),
               sort: sort || sortList[0],
            })
         );
         isSearch.current = true;
      }
   }, []);

   const pizzas = items?.map((pizza: any) => (
      <PizzaBlock key={pizza.id} {...pizza} />
   ));

   const skeletons = [...new Array(6)].map((_, idx) => <Skeleton key={idx} />);

   return (
      <div className="container">
         <div className="content__top">
            <Categories
               value={categoryId}
               onChangeCategory={onChangeCategory}
            />
            <Sort value={sort} />
         </div>
         <h2 className="content__title">–í—Å–µ –ø–∏—Ü—Ü—ã</h2>
         {status === "error" ? (
            <div className="content__error-info">
               <h2>–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ üòï</h2>
               <p>
                  –ö —Å–æ–∂–∞–ª–µ–Ω–∏—é, –Ω–µ —É–¥–∞–ª–æ—Å—å –ø–æ–ª—É—á–∏—Ç—å –ø–∏—Ü—Ü—ã. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–æ–≤—Ç–æ—Ä–∏—Ç—å
                  –ø–æ–ø—ã—Ç–∫—É –ø–æ–∑–∂–µ.
               </p>
            </div>
         ) : (
            <div className="content__items">
               {status === "loading" ? skeletons : pizzas}
            </div>
         )}

         <Pagination value={currentPage} onChangePage={onChangePage} />
      </div>
   );
};

export default Home;
